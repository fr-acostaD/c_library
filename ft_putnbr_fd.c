#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n < 10)
	{
		c = n % 10 + 48;
		write(fd, &c, 1);
		return ;
	}
	c = n % 10 + 48;
	ft_putnbr_fd(n /= 10, fd);
	write(fd, &c, 1);
}
